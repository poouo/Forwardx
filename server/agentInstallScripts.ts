    "FORWARDX_CURL_CONNECT_TIMEOUT=\"${FORWARDX_CURL_CONNECT_TIMEOUT:-15}\"",
    "FORWARDX_CURL_LOW_SPEED_LIMIT=\"${FORWARDX_CURL_LOW_SPEED_LIMIT:-1024}\"",
    "FORWARDX_CURL_ASSET_LOW_SPEED_TIME=\"${FORWARDX_CURL_ASSET_LOW_SPEED_TIME:-180}\"",
