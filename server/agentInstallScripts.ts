    '    systemctl disable "$NAME" 2>/dev/null || true',
    '    rm -f "/etc/systemd/system/$NAME.service"',
    '    systemctl daemon-reload 2>/dev/null || true',
    '  fi',
    '  if command -v rc-service >/dev/null 2>&1; then rc-service "$NAME" stop 2>/dev/null || true; fi',
    '  if command -v rc-update >/dev/null 2>&1; then rc-update del "$NAME" default 2>/dev/null || true; fi',
    '  if [ -x "/etc/init.d/$NAME" ]; then "/etc/init.d/$NAME" stop 2>/dev/null || true; fi',
    '  command -v update-rc.d >/dev/null 2>&1 && update-rc.d -f "$NAME" remove >/dev/null 2>&1 || true',
    '  command -v chkconfig >/dev/null 2>&1 && chkconfig "$NAME" off >/dev/null 2>&1 || true',
    '  rm -f "/etc/init.d/$NAME"',
    '}',
    '',
    'show_help() {',
    '  echo "======================================"',
    '  echo "  ForwardX Agent 管理工具"',
    '  echo "======================================"',
    '  echo ""',
    '  echo "用法:"',
    '  echo "  安装 Agent:"',
    '  echo "    curl -sL PANEL_URL/api/agent/install.sh | bash -s -- install YOUR_TOKEN"',
    '  echo ""',
    '  echo "  卸载 Agent:"',
    '  echo "    curl -sL PANEL_URL/api/agent/install.sh | bash -s -- uninstall"',
    '  echo ""',
    '  echo "  升级 Agent:"',
    '  echo "    curl -sL PANEL_URL/api/agent/install.sh | bash -s -- upgrade"',
    '  echo ""',
    '  echo "参数:"',
    '  echo "  install   <TOKEN>  安装 Agent 并注册到面板"',
    '  echo "  upgrade   [TOKEN]  升级 Agent，默认复用现有配置"',
    '  echo "  uninstall          完全卸载 Agent 及相关组件"',
    '  echo ""',
    '}',
    '',
    '# ============ 工具函数 ============',
    '',
    'resolve_token() {',
    '  if [ -n "$TOKEN" ]; then',
    '    AGENT_TOKEN="$TOKEN"',
    '  else',
    '    echo "[错误] 缺少 Agent Token"',
    '    echo "用法: curl -sL PANEL_URL/api/agent/install.sh | bash -s -- install YOUR_TOKEN"',
    '    exit 1',
    '  fi',
    '}',
    '',
    'detect_arch() {',
    '  case "$(uname -m)" in',
    '    x86_64|amd64) echo "amd64" ;;',
    '    aarch64|arm64) echo "arm64" ;;',
    '    *) echo "" ;;',
    '  esac',
    '}',
    '',
