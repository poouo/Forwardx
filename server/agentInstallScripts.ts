    '  local URL="$1"',
    '  if is_github_accelerator_enabled; then',
    '    case "$URL" in',
    '      http://github.com/*|https://github.com/*|http://raw.githubusercontent.com/*|https://raw.githubusercontent.com/*)',
    '        printf "%s/%s\\n" "$GITHUB_ACCELERATOR_URL" "$URL"',
    '        return 0',
