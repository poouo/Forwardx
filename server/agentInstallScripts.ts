    '    if download_panel_binary "$URL" "$DST" "$LABEL"; then',
    '      DOWNLOADED_RELEASE_VERSION="$RELEASE_VERSION"',
    '      return 0',
    '    else',
    '      PANEL_RC=$?',
    '    fi',
    '  fi',
    '  if [ "$GITHUB_RC" = "2" ]; then',
    '    return 2',
    '  fi',
    '  return 1',
    '}',
