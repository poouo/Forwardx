    '',
    '[Service]',
    'Type=simple',
    'ExecStart=$GO_AGENT_BIN -config $CONFIG_DIR/config.json',
    'Restart=always',
    'RestartSec=10',
    '',
    '[Install]',
    'WantedBy=multi-user.target',
    'EOF',
    '    systemctl daemon-reload',
    '    systemctl enable "$SERVICE_NAME"',
    '    echo "[信息] systemd 服务已创建"',
    '  elif command -v rc-service >/dev/null 2>&1 && command -v rc-update >/dev/null 2>&1; then',
    '    write_agent_openrc_service',
    '    rc-update add "$SERVICE_NAME" default >/dev/null 2>&1 || true',
    '    echo "[信息] OpenRC 服务已创建"',
    '  elif [ -d /etc/init.d ]; then',
    '    write_agent_sysv_service',
    '    command -v update-rc.d >/dev/null 2>&1 && update-rc.d "$SERVICE_NAME" defaults >/dev/null 2>&1 || true',
    '    command -v chkconfig >/dev/null 2>&1 && chkconfig "$SERVICE_NAME" on >/dev/null 2>&1 || true',
    '    echo "[信息] SysV 服务已创建"',
    '  else',
    '    echo "[错误] 不支持的 init 系统，请安装 systemd、OpenRC 或 SysV init"',
    '    return 1',
    '  fi',
    '}',
    '',
    'start_agent_service() {',
    '  if is_systemd_host; then',
    '    if ! systemctl restart "$SERVICE_NAME"; then',
    '      echo "[错误] systemd 服务启动失败"',
    '      systemctl status "$SERVICE_NAME" --no-pager -l 2>/dev/null || true',
    '      return 1',
    '    fi',
    '    sleep 1',
    '    if ! systemctl is-active --quiet "$SERVICE_NAME"; then',
    '      echo "[错误] systemd 服务未保持运行"',
    '      systemctl status "$SERVICE_NAME" --no-pager -l 2>/dev/null || true',
    '      journalctl -u "$SERVICE_NAME" -n 50 --no-pager 2>/dev/null || true',
