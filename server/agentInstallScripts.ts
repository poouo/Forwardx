    '    *) return 1 ;;',
    '  esac',
    '}',
    '',
    'is_github_accelerator_enabled() {',
    '  is_enabled_value "${GITHUB_ACCELERATOR_ENABLED:-}" && [ -n "${GITHUB_ACCELERATOR_URL:-}" ]',
